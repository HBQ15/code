public class ReplaceTester
{
   public static void main(String[] args)
   {
      String greeting = "Mississippi!";
      //Replace "i" with "!", "s" with "$".
      

      System.out.println(greeting.replace("Mississippi" , "M1$$1$$1pp$"));
      System.out.println("Expected: M1$$1$$1pp1");
   }
}
